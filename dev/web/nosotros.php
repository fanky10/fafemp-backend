                    <h3>Principios del Foro</h3>
                    <p class="text-justify"><b>1° Propósito de bien común;</b> rechazando el mercantilismo que incide en
                        la educación superior y en la salud de los argentinos; priorizando lo social a lo individual;
                        anteponiendo a todo, la relevancia y pertinencia de la misión, funciones y actividades de una
                        Universidad socialmente inclusiva; y postulando una cultura institucional proactiva y dinámica,
                        que hoy demanda a los otros actores sociales y a los poderes del Estado, el consenso necesario
                        para el desarrollo de políticas que trasciendan lo coyuntural, procurando instaurar en definitiva,
                        un nuevo "pacto social" o "contrato moral", donde cada sector comprometa recursos y esfuerzos para
                        hacer realidad las necesarias transformaciones, con una mirada plural y en un contexto cultural
                        múltiple.
                    </p>
                    <p class="text-justify"><b>2° Universalidad en el acceso</b> a la oportunidad de aprendizajes para 
                        cualquier miembro de la sociedad argentina, no sólo para poder ingresar sino permanecer
                        y graduarse en la profesión que se eligió, con la única exigencia de demostrar la capacidad
                        inherente a esos fines; dejando sentado ante el conjunto social que a las carreras en ciencias
                        de la salud, se las considera destinadas a que los estudiantes se conviertan en personas de 
                        bien, en ciudadanos informados y profundamente motivados, capaces de un sentido crítico y aptos 
                        para analizar los problemas comunitarios, buscar y aplicar soluciones, y asumir responsabilidades
                        sociales.
                    </p>
                    <p class="text-justify"><b>3° Carácter retributivo de sus servicios,</b> dado que los esfuerzos 
                        y aportes de la ciudadanía argentina son los que sostienen moral y materialmente a la Universidad
                        Pública, la Academia debe orientarse a servir a todo el Pueblo de la Nación,
                        enfocando sus principales problemas de salud, con énfasis en los grupos más vulnerables
                        y desposeídos, luchando contra la inequidad y la injusticia social, para cumplir así con 
                        su inevitable destino de organismo socialmente solidario.
                    </p>
                    <p class="text-justify"><b>4° Calidad al servicio de la gente,</b> considerando a la excelencia
                        académica como instrumento al servicio del ser humano y, dado que parte de la misión de las
                        instituciones miembro supone responder a expectativas comunitarias, consideran pertinente el
                        cumplimiento de los deberes de: formar profesionales de la salud científica, humana y
                        moralmente calificados; procurar el mayor conocimiento posible del perfil de salud-enfermedad
                        de sus comunidades; mejorar su capacidad de intervenir positivamente sobre los problemas 
                        socio-sanitarios del pueblo; generar ambientes de trabajo sanos en sus comunidades educativas,
                        incluyendo la elevación moral e intelectual de docentes, trabajadores, estudiantes y graduados. 
                    </p>
                    <p class="text-justify"><b>5° Gratuidad de la enseñanza de grado:</b> la escuela pública en 
                        ciencias de la salud en el marco de su libertad y autonomía, asume el compromiso de fortalecer
                        su rol en el ejercicio de este derecho social, promoviendo la universalización de la
                        educación superior y garantizando el no arancelamiento de las carreras de grado; esto,
                        dado que asume a la educación universitaria como vector de redistribución de riquezas 
                        y factor de movilidad social vertical, así también por utilizar como espacio de aprendizaje
                        a servicios públicos de salud, cuya atención es universal y gratuita. 
                    </p>
                    <p class="text-justify"><b>6° Defensa de la calidad institucional,</b> sabiendo que un 
                        organismo universitario creativo, innovador, intelectualmente crítico y transformador
                        de realidades, sólo puede asentar en una Casa autónoma; habitada por personas con
                        libertad de pensar sentir y emitir juicios sin restricciones dentro de sus normas y
                        principios; con plena participación de todos sus factores en el gobierno institucional;
                        todo ello asentando en una institución sana, bañada en un clima interno tolerante e 
                        integrado al conjunto social.  
                    </p>
